def test_kidney_plot_no_truth_config():
    """Test that kidney plots default to show_truth=False."""
    from unittest.mock import patch

    from analysis.kidney_atlas.utils.standardized_plotting import generate_kidney_panels

    scores_df = pd.DataFrame(
        {
            "gene": ["Gene1", "Gene2"],
            "Spatial_Score": [0.5, 0.7],
            "Coverage": [0.3, 0.4],
            "abstain_flag": [False, False],
        }
    )

    # Mock make_standard_plot_set to capture config
    with patch(
        "analysis.kidney_atlas.utils.standardized_plotting.make_standard_plot_set"
    ) as mock_plot:
        from pathlib import Path

        generate_kidney_panels(
            scores_df=scores_df,
            labels=None,
            output_dir=Path("/tmp/test"),
            plot_config={},
        )

        # Verify it was called with truth_col=None (no ground truth for kidney data)
        assert mock_plot.called
        call_kwargs = mock_plot.call_args[1]
        assert call_kwargs["truth_col"] is None
        assert call_kwargs["debug"] is False
