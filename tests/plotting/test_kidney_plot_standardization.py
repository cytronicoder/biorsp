"""Tests for kidney plot standardization.

Ensures kidney plotting produces the same canonical figure set as benchmarks
by delegating to biorsp.plotting.standard.make_standard_plot_set.
"""

import pandas as pd


def test_kidney_plot_creates_required_filenames(tmp_path):
    """Test that kidney plotting creates canonical figure set."""
    import numpy as np

    from analysis.kidney_atlas.utils.standardized_plotting import generate_kidney_panels

    # Create minimal synthetic scores dataframe
    scores_df = pd.DataFrame(
        {
            "gene": [f"Gene{i}" for i in range(20)],
            "Spatial_Score": np.random.rand(20),
            "Coverage": np.random.uniform(0.1, 0.5, 20),
            "abstain_flag": [False] * 18 + [True] * 2,
        }
    )

    # Generate plots
    generate_kidney_panels(
        scores_df=scores_df,
        labels=None,  # No truth labels for kidney
        output_dir=tmp_path,
        plot_config={"show_truth": False},
    )

    # Verify required files exist (PNGs only - no PDFs generated by make_standard_plot_set)
    required_files = [
        "fig_cs_scatter.png",
        "fig_cs_marginals.png",
        "fig_confusion_or_composition.png",  # Should be composition since no labels
    ]

    for filename in required_files:
        filepath = tmp_path / filename
        assert filepath.exists(), f"Missing required file: {filename}"
        assert filepath.stat().st_size > 1000, f"File too small: {filename}"


def test_kidney_plot_delegate_to_standard():
    """Test that kidney plotting delegates to make_standard_plot_set."""
